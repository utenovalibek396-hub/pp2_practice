x = 10
y = 5

print(x > y)        # True
print(x == y)       # False
print(x != y)       # True
print((x > 0) and (y < 10))  # True
print(not (x < y))  # True

# Equality operators: ==(Equal to) , !=(Not equal to)
# Relational operators: 
# >(Greater than), <(less than), >=(Greater than or equal to), <=(Less than or equal to)
